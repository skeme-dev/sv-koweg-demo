<div class="w-full flex flex-col space-y-6">
	<h1 class="text-4xl font-bold">Satzung</h1>
	<h2 class="text-2xl font-semibold text-accent">
		Eine Satzung muss individuell für einen Verein erarbeitet werden. Sie muss auf einen Verein mit
		seinen Organen, seiner Organisation 'zugeschnitten' werden. Ein Verein muss sich seine Satzung
		unter Mitwirkung seiner Gremien erarbeiten.
	</h2>
	<article class="text-lg space-y-3 text-justify">
		<p>
			Bei diesem Prozess ist der Landessportbund Nordrhein-Westfalen e.V. mit seinen zahlreichen
			Beratungsangeboten behilflich. Auf Wunsch können erfahrene Vereinsberater Vereine bei der
			Erarbeitung der Gründungssatzung oder der Überarbeitung ihrer Satzung unterstützen.
		</p>
		<p>
			Für eine Vereinssatzung schreibt das BGB zwingende Mindestinhalte vor. Wenn eine Satzung den
			gesetzlichen Erfordernissen (der §§ 56-59 BGB) nicht genügt, wird die Anmeldung vom
			Amtsgericht unter Angabe der Gründe zurückgewiesen (siehe § 60 BGB).
		</p>
		<p>Der Verein kann dann nicht die Rechtsfähigkeit nach § 21 BGB erhalten.</p>
		<p>
			Nach §§ 57 Abs. 1, 58 BGB muss die Satzung des rechtsfähigenVereins folgende Mindestregelungen
			enthalten:
		</p>
		<ul class="pl-12 list-disc">
			<li>den Vereinsnamen</li>
			<li>den Vereinssitz</li>
			<li>den Vereinszweck</li>
		</ul>
		<p>
			bei Neugründungen soll sich ergeben, dass der Verein in das Vereinsregister eingetragen werden
			soll. Die erfolgte Anmeldung des Vereins ersetzt nicht die Anmerkung in der Satzung, wonach
			angemeldet werden soll.
		</p>
		<ul class="pl-12 list-disc">
			<li>Bestimmungen über den Ein- und Austritt von Mitgliedern</li>
			<li>
				eine Regelung, ob Mitglieder Beiträge zu leisten haben, und wenn: welche Beiträge zu leisten
				sind
			</li>
			<li>die Bildung und Wahl des Vorstandes</li>
			<li>Voraussetzungen und Form der Einberufung von Mitgliederversammlungen</li>
			<li>die Beurkundung der gefassten Beschlüsse</li>
		</ul>
		<p>
			Aus steuerlichen Gründen, aus Fördergesichtspunkten oder aus Gründen einer optimalen
			Vereinsführung sollten – je nach Zielsetzung des Vereins – weitere Regelungen in die Satzung
			aufgenommen werden. Da hier für jeden Verein unterschiedliche Aspekte zu berücksichtigen sind,
			ist von der Übernahme einer „ Mustersatzung“ ohne weitere Überprüfung und Anpassung an den
			eigenen Verein abzuraten.
		</p>

		<p>
			Zusätzlich ist zu beachten, dass auch einige Fachverbände bei Neugründung von Vereinen
			bestimmte Satzungsformulierungen als Voraussetzung für die Aufnahme im Verband fordern. Diese
			sollten rechtzeitig beim jeweiligen Fachverband erfragt werden.
		</p>
		<p>
			Weitere organisatorische Fragen können in Vereinsordnungen geregelt werden. Sie dürfen alle
			Be­stimmungen enthalten, die nicht zu den Grundentscheidungen des Vereins gehören, und können
			die Grundregelungen der Satzung näher ausführen. Der Begriff „Vereinsordnung“ ist gesetzlich
			nicht ge­regelt. Für den Erlass einer ereinsordnung bedarf es in der Satzung einer so
			genannten „Ermächtigungsgrundlage“. Das heißt, die Satzung muss die Grundlagen für die
			Vereinsordnungen regeln.
		</p>
		<p>
			Schon bei der Satzungserstellung ist fachkundige Beratung z. B. durch Berater/-innen des
			Landessportbundes (VIBSS) häufig sinnvoll.
		</p>
		<p>
			Es wird dringend empfohlen, den erstellten Entwurf der Satzung vor Beschlussfassung im Rahmen
			einer Mitgliederversammlung einem fachkundigen Juristen, dem zuständigen Rechtspfleger beim
			registerführenden Amtsgericht und dem Finanzamt zur Überprüfung vorzulegen.
		</p>
		<p>
			Die Rechtspfleger der Amtsgerichte prüfen im Wesentlichen, ob die Mindestanforderungen des BGB
			erfüllt sind und das Finanzamt, ob die Formulierungen zur Erlangung/Erhaltung der
			Gemeinnützigkeit vorliegen. Die Mitarbeiter der Finanzämter nehmen zu der Gründungssatzung,
			einer Satzungs-neufassung oder Satzungsänderungen Stellung und geben Hinweise, wenn die
			Gründungssatzung, die Satzungsneufassung oder die Satzungsänderung nicht die Voraussetzungen
			der Mustersatzung der Abgabenordnung (AO) für Vereine mit den nur aus steuerlichen Gründen
			notwendigen Bestimmungen erfüllen. Vor der Beschlussfassung sollte die Satzung oder
			Satzungsänderung dem Vereinsregister mit der Bitte um eine Stellungnahme übersandt werden. Die
			Rechtspfleger/-innen sind in der Regel sehr hilfsbereit.
		</p>
		<p>
			Erst nach Korrektur, Ergänzungen oder Bestätigung durch Amtsgericht und Finanzamt sollten dann
			die notwendigen Beschlüsse durch die Mitgliederversammlung gefasst und anschließend durch
			einen Notar die Aufnahme/Änderung im Vereinsregister beantragt werden.
		</p>
		<p>
			Es wird nochmals darauf hingewiesen, dass das untenstehende Muster einer Vereinssatzung für
			Sportvereine nur eine Zusammenfassung der gesetzlichen Mindestanforderungen an eine Satzung,
			sowie eventuell sinnvoller Satzungsbausteine für Sportvereine ist. Es handelt sich nicht um
			eine allgemeingültige Mustersatzung. Der Landessportbund Nordrhein-Westfalen e.V. kann keine
			Gewähr dafür leisten, dass Vereinsregistergerichte oder Finanzbehörden nicht auch andere
			Ansichten vertreten.
		</p>
	</article>

